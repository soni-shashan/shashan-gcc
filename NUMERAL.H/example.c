//Decimal To Binary Convertion

void main () {
    system("cls");
    int n;
    printf("Enter A Decimal: ");
    scanf("%d",&n);
    print("Decimal: %d\n",n);
    print("Binary: %b\n",n);
    print("String: %s\n","Hello");
    print("char: %c\n",'C');
    print("Octa Decimal: %o\n",n);
    print("Hexa Decimal: %x\n",n);
    print("Int: %d\n",5);
    print("Float: %f\n",2.5);
    print("Float 2 Decimal: %.2f\n",2.5);
    getch();
    system("cls");
}

